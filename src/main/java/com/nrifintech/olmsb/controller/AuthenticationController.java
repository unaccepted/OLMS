package com.nrifintech.olmsb.controller;

import javax.servlet.http.HttpSession;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.servlet.ModelAndView;
/**
 * This controller is responsible for Authentication of the user
 * @author jayabratas
 *
 */
@Controller
public class AuthenticationController {
	/**
	 * This API is responsible for logout functionality for active users
	 * @param session
	 * @return ModelAndView
	 */
	@RequestMapping(value ="/logout", method = RequestMethod.GET)
	public ModelAndView sessionDestroy(HttpSession session) {
		session.removeAttribute("userID");
		session.invalidate();
		
		ModelAndView model = new ModelAndView("index");
		return model;
	}

}
